            <h2 class="display-5 link-body-emphasis mb-1">Kegiatan Praktek Industri 2024</h2>
            <p class="blog-post-meta">Mei 15, 2024 by <a href="#">Admin</a></p>

            <p>Praktek Industri adalah bagian integral dari kurikulum di SMK Negeri 5 Padang. Tahun ini, siswa-siswi
                kami berkesempatan untuk belajar langsung di perusahaan-perusahaan terkemuka di Indonesia.</p>
            <blockquote>
                <p>Dengan bimbingan dari profesional industri, siswa dapat menerapkan pengetahuan yang mereka peroleh di
                    kelas ke dunia nyata.</p>
            </blockquote>
            <p>Kegiatan ini tidak hanya memberikan pengalaman kerja nyata, tetapi juga membangun jaringan profesional
                yang berharga untuk masa depan siswa.</p>
            <h3>Daftar Perusahaan Mitra</h3>
            <p>Berikut adalah beberapa perusahaan mitra yang bekerja sama dengan kami:</p>
            <ul>
                <li>PT Telkom Indonesia</li>
                <li>PT Astra International</li>
                <li>Bank Nagari</li>
                <li>PT Semen Padang</li>
            </ul>
            <p>Kami berharap kegiatan ini dapat memberikan wawasan dan keterampilan baru bagi siswa kami.</p>
        </article>

        <!-- Artikel 3: Prestasi Siswa di Olimpiade Sains -->
        <article class="blog-post">
            <h2 class="display-5 link-body-emphasis mb-1">Prestasi Gemilang di Olimpiade Sains 2024</h2>
            <p class="blog-post-meta">April 20, 2024 by <a href="#">Admin</a></p>

            <p>SMK Negeri 5 Padang bangga mengumumkan bahwa siswa kami berhasil meraih medali emas di Olimpiade Sains
                Nasional 2024.</p>
            <p>Siswa kami, Andi Saputra, memenangkan kompetisi dalam kategori Fisika dengan menunjukkan pengetahuan dan
                keterampilan luar biasa.</p>
            <p>Prestasi ini adalah hasil dari dedikasi dan kerja keras siswa serta bimbingan dari para guru yang
                kompeten di SMK Negeri 5 Padang.</p>
            <blockquote>
                <p>"Kami berkomitmen untuk terus mendukung dan mengembangkan potensi siswa dalam berbagai bidang
                    keilmuan." - Kepala Sekolah</p>
            </blockquote>
            <p>Kami berharap prestasi ini dapat menjadi inspirasi bagi siswa lain untuk terus berprestasi di bidang
                masing-masing.</p>
